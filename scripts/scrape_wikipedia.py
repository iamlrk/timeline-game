# ── Year parsing ───────────────────────────────────────────────────────────────

_YEAR_RE = re.compile(
    r"""
    (?:^|\s|[(\[])
    (c\.?\s*)?                      # circa
    (\d{1,4})                       # year digits
    \s*
    (BC|BCE|AD|CE)?                 # era
    (?:\s|$|[)\]])
    """,
    re.VERBOSE | re.IGNORECASE,
)


def parse_year(text: str):
    """Return (year: int, month: int|None, day: int|None) or None."""
    # Try ISO date first: YYYY-MM-DD
    m = re.search(r"\b(\d{4})-(\d{2})-(\d{2})\b", text)
    if m:
        return int(m.group(1)), int(m.group(2)), int(m.group(3))

    # Try "Month DD, YYYY"
    months = {"january":1,"february":2,"march":3,"april":4,"may":5,"june":6,
              "july":7,"august":8,"september":9,"october":10,"november":11,"december":12}
    m = re.search(r"(\w+)\s+(\d{1,2}),?\s+(\d{4})", text, re.IGNORECASE)
    if m and m.group(1).lower() in months:
        return int(m.group(3)), months[m.group(1).lower()], int(m.group(2))

    # Generic year + era
    m = _YEAR_RE.search(text)
    if m:
        y = int(m.group(2))
        era = (m.group(3) or "AD").upper()
        if "BC" in era or "BCE" in era:
            y = -y
        return y, None, None

    return None


